                                        ¿Qué es una API?
                                    </h1>
                                    <img src="img/rest.png" width="245" height="200" align="left" alt="">
                                    <p>
                                        El término API es una abreviatura de Application Programming Interfaces, que
                                        en español significa interfaz de programación de aplicaciones. Se trata de un
                                        conjunto de definiciones y protocolos que se utiliza para desarrollar e integrar
                                        el software de las aplicaciones, permitiendo la comunicación entre dos aplicaciones
                                        de software a través de un conjunto de reglas.
                                    </p>
                                    <p>
                                        Así pues, podemos hablar de una API como una especificación formal que establece cómo
                                        un módulo de un software se comunica o interactúa con otro para cumplir una o muchas
                                        funciones. Todo dependiendo de las aplicaciones que las vayan a utilizar, y de los permisos
                                        que les dé el propietario de la API a los desarrolladores de terceros.
                                    </p>
                                    <p>
                                        Como hemos dicho antes, las API pueden tener tanto una como varias funciones, pudiendo llegar
                                        a ser auténticos kits de herramientas. Cuando esto pasa, tu aplicación puede enviarle una
                                        solicitud con una estructura particular, y esta estructura determinará cómo responderá el servicio
                                        o el software al que le estés enviando esa solicitud.
                                    </p>
                                    <p>
                                        Pueden ser privadas para el uso de una empresa, abiertas sólo para partners, o públicas para
                                        que cualquier desarrollador interactuar con ellas o crear sus propias API para que lo hagan.
                                        También pueden ser API locales para aplicaciones que se comunican dentro de un mismo ambiente o
                                        dispositivo, o remotas para cuando hay que acceder a otro punto diferente.
                                    </p>
