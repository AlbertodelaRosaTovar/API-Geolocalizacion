                                    <h1>
                                        ¿Qué es geolocalización?
                                    </h1>
                                    <img src="img/que.jpg" width="245" height="200" align="left" alt="">
                                    <p>
                                        La geolocalización puede referirse a la consulta de la ubicación, o bien para la consulta real de la ubicación. Está estrechamente relacionada con el uso de sistemas de posicionamiento, pero puede distinguirse de estos por un mayor énfasis en la determinación de una posición significativa, y no sólo por un conjunto de coordenadas geográficas
                                    </p>
                                    <p>
                                        De acuerdo con Investopedia, la geolocalización “es la capacidad de rastrear el paradero de un dispositivo utilizando GPS, torres de teléfonos celulares, puntos de acceso WiFi o una combinación de estos”.
                                        Dado que los dispositivos son utilizados por individuos, “la geolocalización utiliza sistemas de posicionamiento para rastrear el paradero de un individuo hasta coordenadas de latitud y longitud. Incluso una dirección física”.
                                    </p>
                                    <br>
                                    <br>
                                    <h1>
                                        Propósitos de la geolocalización
                                    </h1>
                                    <ol>
                                        <li>Georeferenciación o posicionamiento. Determina la ubicación física de un objeto o una persona con relación a un sistema de coordenadas (mapa) para, posteriormente, acceder a información específica. Ejemplos de esto son la navegación vehicular a través de un dispositivo de sistema de posicionamiento global (GPS) y vigilancia de prisioneros a través de dispositivos de seguridad habilitados con GPS para los tobillos.
                                        </li>
                                        <br>
                                        <li>Geocodificación. Busca información sobre objetos o servicios en un mapa, por ejemplo, localiza un restaurante que ofrece un tipo particular de comida.sa conocer mejor a su público objetivo, entendiendo mejor sus necesidades, preferencias y patrones de consumo.
                                        </li>
                                        <br>
                                        <li> Geoetiquetado. Agrega información geográfica a un objeto, como una fotografía, mediante la incorporación de datos de geolocalización en los metadatos de la fotografía.
                                        </li>
                                    </ol>


                                    <h1>
                                        Uso
                                    </h1>
                                    <p>Utilizando un dispositivo conectado a Internet, con la geolocalización es posible obtener distintos tipos de información en tiempo real y localizarlos en el mapa con una gran precisión en un momento determinado en el tiempo. Los datos de geolocalización se pueden recopilar de muchas formas:</p>
                                    <ul>
                                        <li>
                                            Mediante la navegación web a través de direcciones IP.
                                        </li>
                                        <li>
                                            Teléfonos móviles.
                                        </li>
                                        <li>
                                            Dispositivos GPS.
                                        </li>
                                        <li>
                                            Identificación de radiofrecuencia (RFID).
                                        </li>
                                        <li>
                                            Transacciones con tarjeta de crédito o débito.
                                        </li>
                                        <li>
                                            Etiquetas en fotografías y mensajes (tales como las geoetiquetas o verificaciones mediante aplicaciones como Foursquare) en sitios de redes sociales como Facebook y Twitter.
                                        </li>
                                        <img src="img/geo.jpg" width="245" height="200" align="right" alt="">
                                    </ul>

                                    <p>La tecnología de geolocalización se ha convertido en una base para los servicios de posicionamiento por ubicación y las aplicaciones sensibles a la ubicación que se ejecutan en los teléfonos inteligentes.</p>

                                    <p>Los datos de geolocalización tienen una variedad de usos, cada uno de los cuales se puede personalizar para determinadas aplicaciones, entornos o empresas. Actualmente estos usos incluyen la ubicación y/o personalización de los contenidos distribuidos, la aplicación de restricciones de acceso y distribución de acuerdo a la ubicación geográfica, prevención de fraudes y análisis de tráfico de redes.</p>
                                    <br>

                                    <h1>Seguridad y privacidad relacionada con la geolocalización</h1>
                                    <p>La ampliación de estas tecnologías y su demanda conlleva los problemas de la naturaleza de la información, a menudo privada y/o sensible, asociados a estas tecnologías. Por lo tanto, es importante conocer, particularmente, los problemas relativos a la seguridad y a la privacidad para poder utilizar las herramientas de geolocalización de manera responsable.</p>

                                    <p>Los datos de geolocalización son generados y recopilados de cualquiera de las dos formas siguientes: en un modo activo conocido como geolocalización basada en el dispositivo del usuario o un modo pasivo conocido como búsqueda en tabla o geolocalización basada en servidores por correlación de datos. </p>

                                    <h1>Impactos de la geolocalización</h1>
                                    <p>La capacidad para proporcionar datos de georeferenciación precisos y oportunos, elementos de etiquetas de interés con metadatos de ubicación y para usar las coordenadas de ubicación como clave para las bases de datos de búsqueda, se ha convertido en la base de un mercado de software creciente para aplicaciones que se ejecutan en plataformas móviles.</p>

