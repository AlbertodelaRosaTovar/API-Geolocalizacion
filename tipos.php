                                    <img src="img/gps.png" width="245" height="200" align="left" alt="">
                                    <h1>GPS</h1>
                                    <p>
                                        Global Positioning System, GPS en resumen, es un sistema de navegación por radio basado en satélite que consta de aproximadamente treinta satélites orbitando la Tierra. Originalmente fue desarrollado para la navegación militar. No obstante, hoy cualquier persona con un dispositivo GPS puede recibir señales de radio que estos satélites transmiten.
                                    </p>
                                    <p>
                                        Una gran ventaja del GPS es su precisión. Puede rastrear sus activos con GPS con mucha precisión, ya que puede localizar algo de hasta cinco metros con precisión. Además, funciona en todas partes al aire libre y no se requiere infraestructura específica.
                                    </p>
                                    <p>La desventaja es el hecho de que esta tecnología de geolocalización requiere bastante energía porque se comunica con varios satélites alrededor de la Tierra. Otras desventajas son que la comunicación de larga distancia a menudo se interrumpe por situaciones relacionadas con el clima y su incapacidad para trabajar en interiores.</p>
                                    <p>En los smartphones, muchas aplicaciones utilizan la geolocalización por GPS. Para ello, los usuarios pueden aceptar</p>
                                    <br>


                                    <img src="img/wifi.jpg" width="245" height="200" align="right" alt="">
                                    <h1>
                                        WiFi
                                    </h1>
                                    <p>El posicionamiento WiFi aprovecha las redes inalámbricas de área local (WLAN), que son redes de dispositivos que se conectan a una radiofrecuencia específica. Esto puede ser de 2,4 GHz o 5,0 GHz. A continuación, el dispositivo transfiere datos a través de ondas de radio durante un alcance de hasta cien metros. Ello significa que wi-fi puede cubrir sitios tanto en interiores como en exteriores.
                                    </p>
                                    <p>El posicionamiento WiFi también aprovecha las redes WiFi a las que no posees o a las que no puedes acceder. El dispositivo que se conexte WiFi puede realizar un seguimiento de la información pública sobre esas redes, como direcciones IP y BSSID, para determinar la ubicación.</p>

                                    <br>

                                    <h1>Geolocalización basada en la red</h1>

                                    <ul>
                                        <li>
                                            La ubicación también se puede determinar mediante la infraestructura de red de un proveedor de servicios. La precisión de las técnicas basadas en red puede variar. Esto depende tanto de la concentración de estaciones base como de la implementación de los métodos de sincronización más actualizados.

                                        </li>
                                        <li>
                                            Una técnica utilizada por diferentes proveedores de red es la triangulación de red. Esto significa que se determina la ubicación de un punto formando triángulos a él a partir de puntos conocidos. Para usar la infraestructura de red de un proveedor de servicios, el dispositivo de seguimiento estará equipado con un módulo del proveedor de servicios.

                                        </li>
                                        <li>
                                            De todas las tecnologías de geolocalización analizadas, la geolocalización basada en la red requiere la menor energía. La precisión de esta técnica de posicionamiento depende de la red y la densidad de las estaciones base disponibles (normalmente más altas en un entorno urbano en comparación con un entorno rural) y pueden variar mucho.
